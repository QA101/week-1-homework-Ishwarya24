
public class swap3var {
	public static void main(String[] args)
	{
		int x=10,y=5,z=12,temp1,temp2;
		temp1=x;
		x=z;
		temp2=y;
		y=temp1;
		z=temp2;
		System.out.print("x = ");
		System.out.println(x);
		System.out.print("y = ");
		System.out.println(y);
		System.out.print("z = ");
		System.out.println(z);
		
	}


}
