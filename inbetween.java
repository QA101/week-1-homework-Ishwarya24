
public class inbetween {
public static void main(String[] args)
{
	int x,y,z;
	x=150;
	y=100;
	z=200;
	if(x>y&&x<z)
	{
		System.out.print(x);
	    System.out.print(" is in between ");
	    System.out.print(y);
	    System.out.print(" and ");
	    System.out.print(z);
	}
	else
	{
		System.out.print(x);
	    System.out.print(" is not in between ");
	    System.out.print(y);
	    System.out.print(" and ");
	    System.out.print(z);
	}
}
}
